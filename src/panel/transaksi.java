    protected void datatable(){
        Object[] Baris ={"ID Transaksi","Nama Transaksi","Keterangan Transaksi"};
        tabmode = new DefaultTableModel(null, Baris);
        tabel_p.setModel(tabmode);
        String sql = "select * from transaksi";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String a = hasil.getString("id_transaksi");
                String b = hasil.getString("nm_transaksi");
                String c = hasil.getString("ket_transaksi");
                
                String[] data={a,b,c};
                tabmode.addRow(data);
            }
        } catch (Exception e){}
    }
    
    
